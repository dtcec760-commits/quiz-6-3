def MultiClass(W1,W2,X,D):

    alpha = 0.1

    for x,d in zip(X,D):

        # forward
        v1 = np.matmul(W1,x)
        y1 = Sigmoid(v1)

        v = np.matmul(W2,y1)
        y = Softmax(v)

        # error
        e = d - y

        # backprop
        delta2 = e
        delta1 = np.matmul(W2.T,delta2) * y1*(1-y1)

        # update
        W2 = W2 + alpha*np.matmul(delta2,y1.T)
        W1 = W1 + alpha*np.matmul(delta1,x.T)

    return W1,W2


