import numpy as np


def Sigmoid(x):
    return 1.0 / (1.0 + np.exp(-x))

import numpy as np


def Softmax(x):
    x  = np.subtract(x, np.max(x))        # prevent overflow
    ex = np.exp(x)

    return ex / np.sum(ex)

